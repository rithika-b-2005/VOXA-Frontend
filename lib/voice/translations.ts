// Helper function to get translated text
export const getVoiceText = (
  key: VoiceTranslationKeys,
  language: string = "en-IN",
  replacements?: Record<string, string>
): string => {
  const langCode = language.split('-')[0] // Get base language code (e.g., "en" from "en-US")
  const translations = voiceTranslations[langCode] || voiceTranslations["en"]
  let text = translations[key] || voiceTranslations["en"][key] || key

  if (replacements) {
    Object.entries(replacements).forEach(([placeholder, value]) => {
      text = text.replace(`{${placeholder}}`, value)
    })
  }

  return text
}
